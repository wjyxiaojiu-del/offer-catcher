/**
 * 检查学校是否属于 985/211 名单。
 * 返回加分值：985=5, 211=4, 都不是=0。
 */
export function getUniversityBonus(schoolName: string): number {
  // 精确匹配：检查名单中是否有学校名包含在输入中，或输入包含在学校名中
  for (const uni of UNIVERSITIES_985) {
    if (schoolName.includes(uni) || uni.includes(schoolName)) return 5
  }
  for (const uni of UNIVERSITIES_211) {
    if (schoolName.includes(uni) || uni.includes(schoolName)) return 4
  }
  return 0
}
